                        {
                            Orientation = LayoutOrientation.Vertical,
                            Visible = false,
                            Margin = new Thickness(20, 5, 0, 0)
                        };

                        toggleButton.OnPressed += _ =>
                        {
                            subRolesContainer.Visible = !subRolesContainer.Visible;
