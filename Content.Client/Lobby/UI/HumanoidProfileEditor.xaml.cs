        private string GetLinkDisplayText(string url)
        {
            if (url.Length > 40)
            {
                return url[..37] + "...";
            }
            return url;
        }

        private void OpenLink(string url)
        {
            if (url.StartsWith("www.", StringComparison.OrdinalIgnoreCase))
                url = "https://" + url;

            var uriOpener = IoCManager.Resolve<IUriOpener>();
            uriOpener.OpenUri(url);
        }

